exports = (typeof window === 'undefined') ? global : window;
exports.arraysAnswers = {
  append : function(arr, item) {
    arr.push(item);
    return arr;
  },

  truncate : function(arr) {
    arr.pop();
    return arr;
  },

  prepend : function(arr, item) {
    arr.unshift(item);
    return arr;
  }
};
